// Writes public/_redirects from the STALLION_API_PROXY_TARGET env var so a
// deploy never ships the committed placeholder host by accident.
//
//   Netlify → Site settings → Environment variables:
//     STALLION_API_PROXY_TARGET = https://api.stallion.<yourdomain>
//
// When the variable is unset the committed placeholder is kept, which fails
// closed: the app cannot silently talk to the wrong backend, and the fix is
// one env var — not editing built assets. HTTPS is required; a raw IP or
// http:// target is rejected (see FIXSPEC.md F2).
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const target = (process.env.STALLION_API_PROXY_TARGET || "").trim().replace(/\/+$/, "");

if (!target) {
  console.log(
    "[write-redirects] STALLION_API_PROXY_TARGET not set — keeping committed placeholder _redirects.",
  );
  process.exit(0);
}

if (!/^https:\/\/[a-z0-9.-]+$/i.test(target) || /^https:\/\/\d+\.\d+\.\d+\.\d+$/.test(target)) {
  console.error(
    `[write-redirects] FATAL: STALLION_API_PROXY_TARGET must be an https:// hostname (no IP, no port, no path). Got: ${target}`,
  );
  process.exit(1);
}

const redirects = `# GENERATED by scripts/write-redirects.mjs from STALLION_API_PROXY_TARGET.
# Do not hand-edit in CI — set the env var instead. See FIXSPEC.md F2.
/api/*  ${target}/:splat  200
/*      /index.html       200
`;

const out = join(dirname(fileURLToPath(import.meta.url)), "..", "public", "_redirects");
writeFileSync(out, redirects);
console.log(`[write-redirects] _redirects now proxies /api/* -> ${target}`);
